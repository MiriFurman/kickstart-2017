console.log(typeof undefined);
// 'undefined'
console.log(typeof 4);
// 'number'
console.log(typeof 'Robin Hood');
// 'string'
console.log(typeof function() {});
// 'function'
console.log(typeof true);
// 'boolean'
console.log(typeof {});
// 'object'
console.log(typeof []); // WAT!
// 'object'
console.log(Array.isArray([]));
// 'true'
console.log(typeof null);
// 'object'

